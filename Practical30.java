public class Practical30 {
    public static void main(String[] args) {
        //Return the absolute (positive) value
        System.out.println(Math.abs(-2));

        //Return max value from to value
        System.out.println(Math.max(20,50));

        //Raise power to different number
        System.out.println(Math.pow(2, 10));

        // Return the cube root of value
        System.out.println(Math.cbrt(3));

        // Generate a random number
        System.out.println(Math.random());
    }
}
//output:
// 2
// 50
// 1024.0
// 1.4422495703074083
// 0.660569055395775